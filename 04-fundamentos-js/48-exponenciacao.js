/*
* O ES7 nos trouxe o tão esperando operador de exponenciação! 
* Veio de forma simples e intuitiva facilitar o uso desse cálculo
* matemático para nós, amantes do Javascript.
 */

// Forma antiga
let exp = Math.pow(2,3) // 2³

console.log(exp) // 2 elevado ao cubo é 8

// Forma nova
let novo_exp = 2 ** 3 // 2³

console.log(novo_exp) // Resultado: 8

