// Atribuição simples
const a = 7 // 'constante a recebe valor 7'
let b = 3 // 'variável b recebe valor 3'

// Atibuição somativa
b += a // b = b + a ou b = 3 + 7
console.log(b) // 10

// Atribuição subtrativa
b -= 4 // b = b - 4
console.log(b) // 6

// Atribuição multiplicativa
b *= 2 // b = b * 2
console.log(b) // 12

// Atribuição divisiva
b /= 2 // b = b / 2
console.log(b) // 6

// Atribuição de módulo
b %= 2 // b = b % 2
console.log(b) // 0